package Mediator;

public class Script
{
    private int id;
    private int marks;


    public Script(int id)
    {
        this.id=id;
    }
    public int getId() {
        return id;
    }

    public void setMarks(int marks) {
        this.marks = marks;
    }

    public int getMarks() {
        return marks;
    }
}
